package operaters;

public class AsignmentOperator {

	public static void main(String[] args) {
		int a=2;
		 a +=2; // mean a=a+2;
		
		System.out.println(a);
		
		int b=2;
		b-=1;  //  mean b=b-1;
		
		System.out.println(b);
		
		int c=2;
		c/=2; //   mean c=c/2;
		
		System.out.println(c);
		
		int d=2;
		d*=2; //   mean d=d*2;
		
		System.out.println(d);
		
		
//		all operator are use as so on.

	}

}
